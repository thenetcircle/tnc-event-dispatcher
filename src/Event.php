namespace Tnc\Service\EventDispatcher;

interface Event extends Normalizable
{
    /**
     * @return string
     */
    public function getName();

    /**
     * @param string $name
     *
     * @return $this
     */
    public function setName($name);

    /**
     * @return string
     */
    public function getMessageChannel();

    /**
     * @return string|null
     */
    public function getMessageKey();
}